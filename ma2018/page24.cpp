#include <iostream>
using namespace std;
int main(int argc, char **argv)
{
    cout << "short : " << sizeof(short) << " bytes" << endl;
    cout << "int : " << sizeof(int) << " bytes" << endl;
    cout << "long : " << sizeof(long) << " bytes" << endl;
    cout << "long long: " << sizeof(long long) << " bytes" << endl;
    return 0;
}